#!/usr/bin/env node

import { mkdir, readdir, rm, stat, writeFile, readFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const sourceRoot = process.env.ILLUMIOS_SOURCE_DIR
  ? path.resolve(process.env.ILLUMIOS_SOURCE_DIR)
  : path.resolve(repoRoot, '..', 'illumios');
const outputRoot = path.resolve(repoRoot, 'docs', 'illumios');
const manifestPath = path.resolve(repoRoot, 'data', 'docs-manifest.js');
const vaultPath = path.resolve(repoRoot, 'data', 'docs-vault.js');
const allowedExtensions = new Set(['.md', '.pdf']);

function toPosix(value) {
  return value.split(path.sep).join('/');
}

function titleFromPath(relativePath) {
  const base = path.basename(relativePath, path.extname(relativePath));
  const exact = {
    CLAUDE: 'Claude',
    TASKS: 'Tasks',
    'illumios-academia-prd': 'Illumios Academia PRD',
  };

  if (exact[base]) return exact[base];

  return base
    .replace(/[-_]+/g, ' ')
    .replace(/\b[a-z]/g, (char) => char.toUpperCase())
    .replace(/\bPrd\b/g, 'PRD')
    .replace(/\bGhl\b/g, 'GHL')
    .replace(/\bAi\b/g, 'AI')
    .replace(/\bNj\b/g, 'NJ')
    .replace(/\bLlc\b/g, 'LLC');
}

async function listDocs(rootDir, currentDir = rootDir, docs = []) {
  const entries = await readdir(currentDir, { withFileTypes: true });

  for (const entry of entries) {
    if (entry.name === '.git' || entry.name === 'node_modules') continue;

    const absolutePath = path.join(currentDir, entry.name);
    if (entry.isDirectory()) {
      await listDocs(rootDir, absolutePath, docs);
      continue;
    }

    const ext = path.extname(entry.name).toLowerCase();
    if (!allowedExtensions.has(ext)) continue;

    const relativePath = toPosix(path.relative(rootDir, absolutePath));
    const fileStat = await stat(absolutePath);
    docs.push({
      absolutePath,
      relativePath,
      group: relativePath.startsWith('planning/')
        ? 'planning'
        : relativePath.startsWith('outputs/')
          ? 'outputs'
          : 'root',
      type: ext.slice(1),
      title: titleFromPath(relativePath),
      updatedAt: fileStat.mtime.toISOString(),
      bytes: fileStat.size,
    });
  }

  return docs;
}

async function syncDocs() {
  const sourceStat = await stat(sourceRoot).catch(() => null);
  if (!sourceStat || !sourceStat.isDirectory()) {
    throw new Error(`Illumios source directory not found: ${sourceRoot}`);
  }

  const docs = await listDocs(sourceRoot);
  docs.sort((a, b) => a.relativePath.localeCompare(b.relativePath));

  await rm(outputRoot, { recursive: true, force: true });
  await mkdir(path.dirname(vaultPath), { recursive: true });

  const manifest = docs.map((doc) => ({
    title: doc.title,
    group: doc.group,
    type: doc.type,
    path: doc.relativePath,
    updatedAt: doc.updatedAt,
    bytes: doc.bytes,
  }));

  const output = `// Generated by scripts/sync-illumios-docs.mjs — keep in sync with the source illumios repo.\nexport default ${JSON.stringify(manifest, null, 2)};\n`;
  await writeFile(manifestPath, output, 'utf8');

  const vaultEntries = {};
  for (const doc of docs) {
    const fileBuffer = await readFile(doc.absolutePath);
    vaultEntries[doc.relativePath] = {
      mimeType: doc.type === 'pdf' ? 'application/pdf' : 'text/markdown;charset=utf-8',
      base64: fileBuffer.toString('base64'),
    };
  }
  const vaultOutput = `// Generated by scripts/sync-illumios-docs.mjs — contains the in-app document vault payloads.\nexport default ${JSON.stringify(vaultEntries, null, 2)};\n`;
  await writeFile(vaultPath, vaultOutput, 'utf8');

  const summary = {
    sourceRoot,
    syncedDocs: manifest.length,
    markdown: manifest.filter((doc) => doc.type === 'md').length,
    pdfs: manifest.filter((doc) => doc.type === 'pdf').length,
  };

  process.stdout.write(`${JSON.stringify(summary, null, 2)}\n`);
}

syncDocs().catch((error) => {
  process.stderr.write(`${error.stack || error.message}\n`);
  process.exitCode = 1;
});
